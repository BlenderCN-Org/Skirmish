
namespace Engine.Animation
{
    /// <summary>
    /// Transition description
    /// </summary>
    public class TransitionDescription
    {
        /// <summary>
        /// Clip from name
        /// </summary>
        public string ClipFrom;
        /// <summary>
        /// Clip to name
        /// </summary>
        public string ClipTo;
        /// <summary>
        /// Total duration
        /// </summary>
        public float Duration;
        /// <summary>
        /// Clip from start
        /// </summary>
        public float StartFrom;
        /// <summary>
        /// Clip to start
        /// </summary>
        public float StartTo;
    }
}
